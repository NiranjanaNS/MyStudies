#include <stdio.h>
int main() {
    int a, b;
    printf("Enter first number:\n");
    scanf("%d", &a);
    printf("Enter first number:\n");
    scanf("%d", &b);
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a % b = %d\n", a % b);
    printf("+a = %d\n", +a);
    printf("-a = %d\n", a + b);
    printf("a++ = %d\n", a + b);
    printf("a-- = %d\n", a + b);
    printf("++a = %d\n", a + b);
    printf("--a = %d\n", a + b);
    return 0;
}