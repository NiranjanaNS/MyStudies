#include <stdio.h>
int main() {
    int a=20, b=10;
    if(a < b)
    printf("A is less than B");
    else if(a > b)
    printf("A is greater than B");
    else if(a <= b)
    printf("A is greater than B");
    else if(a >= b)
    printf("A is greater than B");
    else if(a == b)
    printf("A is greater than B");
    else if(a != b)
    printf("A is greater than B");
    else
    printf("Something went wrong");
}